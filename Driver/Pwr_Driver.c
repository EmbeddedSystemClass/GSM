/***************************************************************************************************
*FunctionName：Led_GPIOInit
*Description：初始化系统指示灯的IO
*Input：None
*Output：None
*Author：xsx
*Data：2015年8月26日14:34:45
***************************************************************************************************/
void Pwr_GPIOInit(void)
{
	GPIO_InitTypeDef GPIO_InitStructure;
	RCC_AHBPeriphClockCmd(Pwr_GpioRcc, ENABLE); /*使能LED灯使用的GPIO时钟*/

	GPIO_InitStructure.GPIO_Pin = Pwr_Pin; 
  	GPIO_InitStructure.GPIO_Mode = GPIO_Mode_OUT;
	GPIO_InitStructure.GPIO_OType = GPIO_OType_PP;
	GPIO_InitStructure.GPIO_Speed = GPIO_Speed_10MHz;
	GPIO_InitStructure.GPIO_PuPd = GPIO_PuPd_UP;
	GPIO_Init(Pwr_GpioGroup, &GPIO_InitStructure);
	
	GPIO_WriteBit(Pwr_GpioGroup, Pwr_Pin, Bit_RESET);
}

/***************************************************************************************************
*FunctionName：SetLedState
*Description：改变LED灯的状态
*Input：LED_State --- ON	灯亮
*						OFF	灯灭
*Output：None
*Author：xsx
*Data：2015年8月26日14:42:41
***************************************************************************************************/
void SystemPwrOff(void)
{
	GPIO_WriteBit(Pwr_GpioGroup, Pwr_Pin, Bit_SET);
}

