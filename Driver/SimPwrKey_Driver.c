/***************************************************************************************************
*FunctionName：Led_GPIOInit
*Description：初始化系统指示灯的IO
*Input：None
*Output：None
*Author：xsx
*Data：2015年8月26日14:34:45
***************************************************************************************************/
void PwrKey_GPIOInit(void)
{
	GPIO_InitTypeDef GPIO_InitStructure;
	RCC_AHB1PeriphClockCmd(PwrKey_Rcc, ENABLE); /*使能LED灯使用的GPIO时钟*/

  	GPIO_InitStructure.GPIO_Pin = PwrKey_Pin; 
  	GPIO_InitStructure.GPIO_Mode = GPIO_Mode_OUT;
	GPIO_InitStructure.GPIO_OType = GPIO_OType_PP;
	GPIO_InitStructure.GPIO_Speed = GPIO_Speed_100MHz;
	GPIO_InitStructure.GPIO_PuPd = GPIO_PuPd_NOPULL;
	GPIO_Init(PwrKey_GpioGroup, &GPIO_InitStructure);
}

void SimPwrON(void)
{
	GPIO_WriteBit(PwrKey_GpioGroup, PwrKey_Pin, Bit_RESET);
	
//	GPIO_WriteBit(PwrKey_GpioGroup, PwrKey_Pin, Bit_RESET);
}

void SimPwrOff(void)
{
	GPIO_WriteBit(PwrKey_GpioGroup, PwrKey_Pin, Bit_SET);
}

